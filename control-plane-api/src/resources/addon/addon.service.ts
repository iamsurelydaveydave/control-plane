/**
 * Generate a secure random password
 */
function generatePassword(length: number = 24): string {
  return crypto.randomBytes(length).toString("base64").slice(0, length);
}

/**
 * Get nested value from object using dot notation key
 */
function getNestedValue(obj: Record<string, any>, key: string): any {
  return key.split(".").reduce((o, k) => o?.[k], obj);
}

/**
 * Set nested value in object using dot notation key
 */
function setNestedValue(obj: Record<string, any>, key: string, value: any): void {
  const keys = key.split(".");
  const lastKey = keys.pop()!;
  const target = keys.reduce((o, k) => {
    if (!o[k]) o[k] = {};
    return o[k];
  }, obj);
  target[lastKey] = value;
}

/**
 * Build Helm values from user config based on addon type.
 * Translates user-friendly config (replicas, tls, rootPassword, etc.) to Helm chart values.
 */
function buildHelmValues(
  type: TAddonType,
  config: Record<string, any>,
  baseValues: Record<string, any>
): { values: Record<string, any>; generatedPassword?: string } {
  const values = { ...baseValues };
  let generatedPassword: string | undefined;

  // Handle mongodb-replicaset type (uses nested mongodb key)
  if (type === "mongodb-replicaset") {
    // Ensure mongodb key exists
    if (!values.mongodb) {
      values.mongodb = {};
    }

    // Replicas
    if (config.replicas) {
      values.mongodb.replicaCount = Number(config.replicas);
    }

    // TLS
    if (config.tls === true) {
      values.mongodb.tls = { enabled: true, autoGenerated: true };
    }

    // Root password
    if (config.rootPassword) {
      values.mongodb.auth = { ...values.mongodb.auth, rootPassword: config.rootPassword };
    } else {
      generatedPassword = generatePassword();
      values.mongodb.auth = { ...values.mongodb.auth, rootPassword: generatedPassword };
    }

    // Application user
    if (config.createUser && config.username && config.password) {
      values.mongodb.auth = {
        ...values.mongodb.auth,
        usernames: [config.username],
        passwords: [config.password],
        databases: [config.database || config.username],
      };
    }

    // Storage size
    if (config.storageSize) {
      values.mongodb.persistence = { ...values.mongodb.persistence, size: config.storageSize };
    }

    // Resources
    if (config.resources) {
      values.mongodb.resources = config.resources;
    }

    // DNS configuration (MongoDB Atlas-style subdomain)
    if (config.dns?.enabled && config.dns?.baseDomain) {
      values.dns = {
        enabled: true,
        subdomain: config.dns.subdomain || "",  // Empty = use release name
        baseDomain: config.dns.baseDomain,
        replicaSetName: values.mongodb.replicaSetName || "rs0",
        tlsEnabled: config.tls === true,
      };
    }

    // S3 Backup configuration
    if (config.backup?.enabled && config.backup?.s3?.bucket) {
      values.backup = {
        enabled: true,
        schedule: config.backup.schedule || "0 2 * * *",
        retention: config.backup.retention || 7,
        s3: {
          bucket: config.backup.s3.bucket,
          endpoint: config.backup.s3.endpoint || "",
          region: config.backup.s3.region || "us-east-1",
          prefix: config.backup.s3.prefix || "mongodb-backups",
          accessKeyId: config.backup.s3.accessKeyId || "",
          secretAccessKey: config.backup.s3.secretAccessKey || "",
          existingSecret: config.backup.s3.existingSecret || "",
        },
      };
    }

    return { values, generatedPassword };
  }

  // Database types (MongoDB, PostgreSQL, MySQL, MariaDB, ClickHouse)
  if (isDatabaseType(type)) {
    // Replicas / Architecture
    if (config.replicas) {
      const replicas = Number(config.replicas);
      
      switch (type) {
        case "mongodb":
          if (replicas > 1) {
            values.architecture = "replicaset";
            values.replicaCount = replicas;
          } else {
            values.architecture = "standalone";
          }
          break;
        case "postgresql":
          if (replicas > 1) {
            values.architecture = "replication";
            values.readReplicas = { replicaCount: replicas - 1 };
          } else {
            values.architecture = "standalone";
          }
          break;
        case "mysql":
        case "mariadb":
          if (replicas > 1) {
            values.architecture = "replication";
            values.secondary = { replicaCount: replicas - 1 };
          } else {
            values.architecture = "standalone";
          }
          break;
        case "clickhouse":
          values.replicaCount = replicas;
          values.shards = 1;
          break;
      }
    }

    // TLS
    if (config.tls === true) {
      switch (type) {
        case "mongodb":
          values.tls = { enabled: true, autoGenerated: true };
          break;
        case "postgresql":
          values.tls = { enabled: true, autoGenerated: true };
          break;
        case "mysql":
        case "mariadb":
          values.auth = { ...values.auth, usePasswordFiles: true };
          values.tls = { enabled: true, autoGenerated: true };
          break;
        case "clickhouse":
          values.tls = { enabled: true };
          break;
      }
    }

    // Root password
    if (config.rootPassword) {
      switch (type) {
        case "mongodb":
          values.auth = { ...values.auth, rootPassword: config.rootPassword };
          break;
        case "postgresql":
          values.auth = { ...values.auth, postgresPassword: config.rootPassword };
          break;
        case "mysql":
          values.auth = { ...values.auth, rootPassword: config.rootPassword };
          break;
        case "mariadb":
          values.auth = { ...values.auth, rootPassword: config.rootPassword };
          break;
        case "clickhouse":
          values.auth = { ...values.auth, password: config.rootPassword };
          break;
      }
    } else {
      // Generate password if not provided
      generatedPassword = generatePassword();
      switch (type) {
        case "mongodb":
          values.auth = { ...values.auth, rootPassword: generatedPassword };
          break;
        case "postgresql":
          values.auth = { ...values.auth, postgresPassword: generatedPassword };
          break;
        case "mysql":
          values.auth = { ...values.auth, rootPassword: generatedPassword };
          break;
        case "mariadb":
          values.auth = { ...values.auth, rootPassword: generatedPassword };
          break;
        case "clickhouse":
          values.auth = { ...values.auth, password: generatedPassword };
          break;
      }
    }

    // Application user (optional)
    if (config.createUser && config.username && config.password) {
      switch (type) {
        case "mongodb":
          values.auth = {
            ...values.auth,
            usernames: [config.username],
            passwords: [config.password],
            databases: [config.database || config.username],
          };
          break;
        case "postgresql":
          values.auth = {
            ...values.auth,
            username: config.username,
            password: config.password,
            database: config.database || config.username,
          };
          break;
        case "mysql":
          values.auth = {
            ...values.auth,
            username: config.username,
            password: config.password,
            database: config.database || config.username,
          };
          break;
        case "mariadb":
          values.auth = {
            ...values.auth,
            username: config.username,
            password: config.password,
            database: config.database || config.username,
          };
          break;
      }
    }
  }

  // Redis-like types (Redis, KeyDB, Dragonfly)
  if (isRedisType(type)) {
    // Password
    if (config.redisPassword) {
      switch (type) {
        case "redis":
          values.auth = { ...values.auth, enabled: true, password: config.redisPassword };
          break;
        case "keydb":
          values.password = config.redisPassword;
          break;
        case "dragonfly":
          values.auth = { ...values.auth, enabled: true, password: config.redisPassword };
          break;
      }
    } else {
      // Generate password if not provided
      generatedPassword = generatePassword();
      switch (type) {
        case "redis":
          values.auth = { ...values.auth, enabled: true, password: generatedPassword };
          break;
        case "keydb":
          values.password = generatedPassword;
          break;
        case "dragonfly":
          values.auth = { ...values.auth, enabled: true, password: generatedPassword };
          break;
      }
    }

    // Cluster mode
    if (config.clusterMode === true) {
      const nodes = config.clusterNodes || 3;
      switch (type) {
        case "redis":
          values.architecture = "replication";
          values.replica = { replicaCount: nodes };
          values.sentinel = { enabled: true };
          break;
        case "keydb":
          values.nodes = nodes;
          values.multiMaster = true;
          break;
        case "dragonfly":
          values.replicaCount = nodes;
          break;
      }
    } else {
      switch (type) {
        case "redis":
          values.architecture = "standalone";
          break;
      }
    }
  }

  // Other specific addon configs
  switch (type) {
    case "elasticsearch":
      if (config.replicas) {
        values.master = { ...values.master, replicaCount: config.replicas };
        values.data = { ...values.data, replicaCount: config.replicas };
      }
      break;

    case "minio":
      if (config.rootPassword) {
        values.auth = { ...values.auth, rootPassword: config.rootPassword };
      } else if (!values.auth?.rootPassword) {
        generatedPassword = generatedPassword || generatePassword();
        values.auth = { ...values.auth, rootPassword: generatedPassword };
      }
      break;

    case "rabbitmq":
      if (config.password) {
        values.auth = { ...values.auth, password: config.password };
      } else if (!values.auth?.password) {
        generatedPassword = generatedPassword || generatePassword();
        values.auth = { ...values.auth, password: generatedPassword };
      }
      if (config.username) {
        values.auth = { ...values.auth, username: config.username };
      }
      break;

    case "grafana":
      if (config.adminPassword) {
        values.adminPassword = config.adminPassword;
      } else if (!values.adminPassword) {
        generatedPassword = generatedPassword || generatePassword();
        values.adminPassword = generatedPassword;
      }
      break;
  }

  return { values, generatedPassword };
}

/**
 * Extended connection info that includes DNS-based connection strings.
 */
export type TAddonConnectionInfoExtended = TAddonConnectionInfo & {
  srvConnectionString?: string;  // mongodb+srv:// connection string
  dnsClusterHost?: string;       // e.g., "mydb.db.example.com"
  backupEnabled?: boolean;
  backupSchedule?: string;
};

/**
 * Build connection info based on addon type, Helm values, and generated password.
 */
function buildConnectionInfo(
  addon: TAddon,
  generatedPassword?: string
): TAddonConnectionInfoExtended {
  const catalogEntry = getAddonCatalogEntry(addon.type);
  const template = catalogEntry.connectionTemplate;
  
  // Handle mongodb-replicaset specially (uses nested mongodb key)
  if (addon.type === "mongodb-replicaset") {
    const replicaCount = addon.values?.mongodb?.replicaCount || 3;
    const replicaSetName = addon.values?.mongodb?.replicaSetName || "rs0";
    const headlessService = addon.releaseName + "-mongodb-headless";
    
    // Build replica set hosts
    const hosts: string[] = [];
    for (let i = 0; i < replicaCount; i++) {
      hosts.push(addon.releaseName + "-mongodb-" + i + "." + headlessService + "." + addon.namespace + ".svc.cluster.local:27017");
    }
    
    const username = addon.config?.createUser && addon.config?.username
      ? addon.config.username
      : template?.defaultUsername || "root";
    
    const password = addon.config?.createUser && addon.config?.password
      ? addon.config.password
      : generatedPassword || getNestedValue(addon.values, template?.passwordKey || "mongodb.auth.rootPassword");
    
    const database = addon.config?.database || "admin";
    const tlsEnabled = addon.values?.mongodb?.tls?.enabled || false;
    const tlsParam = tlsEnabled ? "&tls=true" : "";
    
    const connectionInfo: TAddonConnectionInfoExtended = {
      host: hosts[0].split(":")[0],  // Primary host for display
      port: 27017,
      username,
      password,
      connectionString: "mongodb://" + username + ":" + password + "@" + hosts.join(",") + "/" + database + "?replicaSet=" + replicaSetName + tlsParam,
    };
    
    // Add DNS-based connection info if configured
    if (addon.values?.dns?.enabled && addon.values?.dns?.baseDomain) {
      const subdomain = addon.values.dns.subdomain || addon.releaseName;
      const clusterHost = subdomain + "." + addon.values.dns.baseDomain;
      connectionInfo.dnsClusterHost = clusterHost;
      connectionInfo.srvConnectionString = "mongodb+srv://" + username + ":" + password + "@" + clusterHost + "/" + database + "?authSource=admin";
    }
    
    // Add backup info
    if (addon.values?.backup?.enabled) {
      connectionInfo.backupEnabled = true;
      connectionInfo.backupSchedule = addon.values.backup.schedule;
    }
    
    return connectionInfo;
  }
  
  // Build internal K8s DNS hostname
  // For master/primary services, some charts use different naming
  let serviceSuffix = "";
  if (addon.type === "mongodb" && addon.values?.architecture === "replicaset") {
    serviceSuffix = "-headless";
  } else if (addon.type === "postgresql" && addon.values?.architecture === "replication") {
    serviceSuffix = "-primary";
  } else if (["mysql", "mariadb"].includes(addon.type) && addon.values?.architecture === "replication") {
    serviceSuffix = "-primary";
  } else if (addon.type === "redis" && addon.values?.architecture === "replication") {
    serviceSuffix = "-master";
  }
  
  const host = `${addon.releaseName}${serviceSuffix}.${addon.namespace}.svc.cluster.local`;
  const port = template?.portOverride || catalogEntry.defaultPort;

  const connectionInfo: TAddonConnectionInfo = {
    host,
    port,
  };

  // Extract username
  if (template?.defaultUsername) {
    connectionInfo.username = template.defaultUsername;
  }
  if (template?.usernameKey) {
    const username = getNestedValue(addon.values, template.usernameKey);
    if (username) {
      connectionInfo.username = username;
    }
  }
  // Override with app user if created
  if (addon.config?.createUser && addon.config?.username) {
    connectionInfo.username = addon.config.username;
  }

  // Extract password
  if (generatedPassword) {
    connectionInfo.password = generatedPassword;
  } else if (template?.passwordKey) {
    const password = getNestedValue(addon.values, template.passwordKey);
    if (password) {
      connectionInfo.password = password;
    }
  }
  // Override with app user password if created
  if (addon.config?.createUser && addon.config?.password) {
    connectionInfo.password = addon.config.password;
  }

  // Build connection string based on type
  connectionInfo.connectionString = buildConnectionString(addon.type, connectionInfo, addon.config);

  return connectionInfo;
}

/**
 * Build a connection string for the given addon type.
 */
function buildConnectionString(
  type: TAddonType,
  info: TAddonConnectionInfo,
  config?: Record<string, any>
): string {
  const auth = info.username && info.password
    ? `${encodeURIComponent(info.username)}:${encodeURIComponent(info.password)}@`
    : info.password
    ? `:${encodeURIComponent(info.password)}@`
    : "";

  switch (type) {
    case "mongodb": {
      const db = config?.database || "admin";
      return `mongodb://${auth}${info.host}:${info.port}/${db}`;
    }
    case "postgresql": {
      const db = config?.database || "postgres";
      return `postgresql://${auth}${info.host}:${info.port}/${db}`;
    }
    case "mysql":
    case "mariadb": {
      const db = config?.database || "";
      return `mysql://${auth}${info.host}:${info.port}/${db}`;
    }
    case "redis":
    case "keydb":
    case "dragonfly": {
      return info.password
        ? `redis://:${encodeURIComponent(info.password)}@${info.host}:${info.port}`
        : `redis://${info.host}:${info.port}`;
    }
    case "rabbitmq": {
      return `amqp://${auth}${info.host}:${info.port}`;
    }
    case "elasticsearch":
    case "meilisearch":
    case "typesense":
      return `http://${info.host}:${info.port}`;
    case "minio":
      return `http://${info.host}:${info.port}`;
    case "kafka":
      return `${info.host}:${info.port}`;
    case "nats":
      return `nats://${info.host}:${info.port}`;
    default:
      return `http://${info.host}:${info.port}`;
  }
}

