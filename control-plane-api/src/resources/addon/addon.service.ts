  async function stop(id: string): Promise<{ message: string }> {
    const addon = await repo.getById(id);
    if (!addon) {
      throw new NotFoundError("Addon not found.");
    }

    if (addon.status === "stopped") {
      return { message: "Addon is already stopped." };
    }

    // Scale to 0 replicas
    const catalogEntry = getAddonCatalogEntry(addon.type);
    const values = {
      ...addon.values,
      replicaCount: 0,
    };

    try {
      await helm.upgrade(
        addon.releaseName,
        catalogEntry.chart,
        addon.namespace,
        values,
        { wait: true, timeout: "5m" }
      );

      await repo.updateStatus(id, "stopped");
      return { message: `Addon "${addon.name}" stopped.` };
    } catch (error: any) {
      const errorMessage = error instanceof Error ? error.message : String(error);
      logger.log({
        level: "error",
        message: `[Addon] Failed to stop addon ${addon.name}: ${errorMessage}`,
      });
      throw new InternalServerError(`Failed to stop addon: ${errorMessage}`);
    }
  }

