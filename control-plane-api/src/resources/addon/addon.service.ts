  /**
   * Sync addon status with Helm for all deployed addons.
   * Used by scheduled tasks.
   */
  async function syncAllStatuses(): Promise<{ synced: number; errors: number }> {
    let synced = 0;
    let errors = 0;

    const addons = await repo.getByStatus("running");
    addons.push(...(await repo.getByStatus("deploying")));

    for (const addon of addons) {
      try {
        await refreshStatus(addon._id!.toString());
        synced++;
      } catch (error) {
        errors++;
      }
    }

    return { synced, errors };
  }

  /**
   * Scale an addon (update replica count).
   * Only valid for database types and certain other addons.
   */
  async function scale(id: string, replicas: number): Promise<{ message: string }> {
    const addon = await repo.getById(id);
    if (!addon) {
      throw new NotFoundError("Addon not found.");
    }

    // Validate replicas
    if (replicas < 1 || replicas > 7) {
      throw new BadRequestError("Replicas must be between 1 and 7.");
    }

    // Only certain types can be scaled
    const scalableTypes: TAddonType[] = [
      "mongodb", "postgresql", "mysql", "mariadb", "clickhouse",
      "redis", "keydb", "dragonfly",
      "elasticsearch",
    ];

    if (!scalableTypes.includes(addon.type)) {
      throw new BadRequestError(`Addon type "${addon.type}" does not support scaling.`);
    }

    // Check Helm availability
    if (!helm.isAvailable()) {
      throw new InternalServerError("Helm is not available. Cannot scale addon.");
    }

    // Update config with new replica count
    const newConfig = { ...addon.config, replicas };

    // Update the record
    await repo.updateById(id, { config: newConfig });
    await repo.updateStatus(id, "deploying");

    // Rebuild Helm values with new config
    const catalogEntry = getAddonCatalogEntry(addon.type);
    const { values } = buildHelmValues(addon.type, newConfig, addon.values);

    // Run upgrade async
    (async () => {
      try {
        await helm.upgrade(
          addon.releaseName,
          catalogEntry.chart,
          addon.namespace,
          values,
          {
            version: addon.version,
            wait: true,
            timeout: "15m",
          }
        );

        await repo.updateById(id, { values });
        await repo.updateStatus(id, "running");

        logger.log({
          level: "info",
          message: `[Addon] Scaled addon ${addon.name} to ${replicas} replicas`,
        });
      } catch (error: any) {
        const errorMessage = error instanceof Error ? error.message : String(error);
        await repo.updateStatus(id, "failed", errorMessage);
        logger.log({
          level: "error",
          message: `[Addon] Failed to scale addon ${addon.name}: ${errorMessage}`,
        });
      }
    })();

    return { message: `Scaling addon "${addon.name}" to ${replicas} replicas...` };
  }

  /**
   * Get logs for an addon's pods
   */
  async function getLogs(
    id: string,
    options: { tailLines?: number; sinceSeconds?: number } = {}
  ): Promise<{ logs: string[] }> {
    const addon = await repo.getById(id);
    if (!addon) {
      throw new NotFoundError("Addon not found.");
    }

    const k8s = useK8sService();

    // Get pods with label selector matching the Helm release
    const pods = await k8s.list(
      "v1",
      "Pod",
      addon.namespace,
      `app.kubernetes.io/instance=${addon.releaseName}`
    );

    const logs: string[] = [];
    for (const pod of pods) {
      try {
        const podLogs = await k8s.getPodLogs(pod.metadata.name, addon.namespace, {
          tailLines: options.tailLines || 100,
          sinceSeconds: options.sinceSeconds,
        });
        logs.push(`=== ${pod.metadata.name} ===\n${podLogs}`);
      } catch (error: any) {
        logs.push(`=== ${pod.metadata.name} ===\n[Error fetching logs: ${error.message}]`);
      }
    }

    return { logs };
  }

  /**
   * Get Kubernetes events for an addon
   */
  async function getEvents(id: string): Promise<{ events: any[] }> {
    const addon = await repo.getById(id);
    if (!addon) {
      throw new NotFoundError("Addon not found.");
    }

    const k8s = useK8sService();

    // Get events in the addon's namespace related to this release
    const allEvents = await k8s.getEvents(addon.namespace);

    // Filter to events mentioning the release name
    const relevantEvents = allEvents.filter(
      (event: any) =>
        event.involvedObject?.name?.includes(addon.releaseName) ||
        event.metadata?.name?.includes(addon.releaseName)
    );

    // Format events for response
    const events = relevantEvents.map((event: any) => ({
      type: event.type,
      reason: event.reason,
      message: event.message,
      firstTimestamp: event.firstTimestamp,
      lastTimestamp: event.lastTimestamp,
      count: event.count,
      involvedObject: {
        kind: event.involvedObject?.kind,
        name: event.involvedObject?.name,
      },
    }));

    // Sort by last timestamp descending
    events.sort(
      (a, b) =>
        new Date(b.lastTimestamp || 0).getTime() -
        new Date(a.lastTimestamp || 0).getTime()
    );

    return { events };
  }

