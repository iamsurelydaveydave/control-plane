  /**
   * Configure DNS for a mongodb-replicaset addon.
   * Creates SRV and A records for mongodb+srv:// connection strings.
   */
  async function configureDNS(
    id: string,
    options: { subdomain?: string } = {}
  ): Promise<{ message: string; clusterHost?: string; srvConnectionString?: string }> {
    const addon = await repo.getById(id);
    if (!addon) {
      throw new NotFoundError("Addon not found.");
    }

    if (addon.type !== "mongodb-replicaset") {
      throw new BadRequestError("DNS configuration is only available for mongodb-replicaset type.");
    }

    const dns = useDNSService();

    const dnsConfig = await dns.getDBConfig();
    if (!dnsConfig) {
      throw new BadRequestError(
        "DNS is not configured. Please configure DNS settings in Settings > DNS."
      );
    }

    // External access must be enabled — pod IPs are internal and not internet-routable
    const externalAccessEnabled = addon.values?.mongodb?.externalAccess?.enabled;
    if (!externalAccessEnabled) {
      throw new BadRequestError(
        "External access must be enabled before configuring DNS. " +
        "Re-deploy or update this addon with externalAccess.enabled=true " +
        "(NodePort or LoadBalancer). Then retry DNS configuration."
      );
    }

    const replicaCount = addon.values?.mongodb?.replicaCount || 3;
    const replicaSetName = addon.values?.mongodb?.replicaSetName || "rs0";
    const subdomain = options.subdomain || addon.values?.dns?.subdomain || addon.name;
    const serviceType: string = addon.values?.mongodb?.externalAccess?.service?.type || "NodePort";

    const k8s = useK8sService();
    const nodes: Array<{ host: string; port: number }> = [];

    if (serviceType === "LoadBalancer") {
      // Each replica gets its own LoadBalancer service: {releaseName}-mongodb-{i}-external
      for (let i = 0; i < replicaCount; i++) {
        const svcName = `${addon.releaseName}-mongodb-${i}-external`;
        const svc = await k8s.getService(addon.namespace, svcName);
        const ingress = svc?.status?.loadBalancer?.ingress;
        if (ingress?.[0]?.ip) {
          nodes.push({ host: ingress[0].ip, port: 27017 });
        } else if (ingress?.[0]?.hostname) {
          // Some LB providers (AWS) give a hostname rather than an IP.
          // DNS SRV needs an IP for A records; log and skip hostname-only ingress.
          logger.log({
            level: "warn",
            message: `[Addon] LoadBalancer for ${svcName} exposes a hostname, not an IP — cannot create A record.`,
          });
        }
      }
    } else {
      // NodePort: all replicas share node IPs but use different node ports.
      // Get the external IP from the first available K8s node.
      const k8sNodes = await k8s.getNodes();
      const nodeIPs: string[] = [];
      for (const n of k8sNodes) {
        const addresses: Array<{ type: string; address: string }> = (n as any).status?.addresses || [];
        const external = addresses.find((a) => a.type === "ExternalIP")?.address;
        const internal = addresses.find((a) => a.type === "InternalIP")?.address;
        const ip = external || internal;
        if (ip) nodeIPs.push(ip);
      }

      for (let i = 0; i < replicaCount; i++) {
        const svcName = `${addon.releaseName}-mongodb-${i}-external`;
        const svc = await k8s.getService(addon.namespace, svcName);
        const nodePort: number = (svc as any)?.spec?.ports?.[0]?.nodePort || 27017;
        // Prefer the i-th node's IP if available (pods may be on different nodes),
        // fall back to the first node IP for single-node clusters (K3s).
        const ip = nodeIPs[i] ?? nodeIPs[0];
        if (ip) {
          nodes.push({ host: ip, port: nodePort });
        }
      }
    }

    if (nodes.length === 0) {
      throw new BadRequestError(
        "Could not determine MongoDB node IPs from K8s Services. " +
        "Ensure the replica set is running and externalAccess services are ready."
      );
    }

    // Get root password for connection string
    const password = addon.connectionInfo?.password ||
      getNestedValue(addon.values, "mongodb.auth.rootPassword") ||
      "";

    // Create DNS records via Cloudflare
    const result = await dns.setupReplicaSet({
      databaseName: subdomain,
      nodes,
      adminUser: "root",
      adminPassword: password,
      replicaSetName,
    });

    if (!result) {
      throw new InternalServerError("DNS setup returned no result.");
    }

    // Persist DNS config back onto the addon
    await repo.updateById(id, {
      values: {
        ...addon.values,
        dns: {
          enabled: true,
          subdomain,
          baseDomain: dnsConfig.baseDomain,
          clusterHost: result.clusterHost,
          nodeHosts: result.nodeHosts,
          srvConnectionString: result.srvConnectionString,
          records: result.records,
        },
      },
      connectionInfo: {
        ...addon.connectionInfo,
        srvConnectionString: result.srvConnectionString,
        dnsClusterHost: result.clusterHost,
      } as any,
    });

    logger.log({
      level: "info",
      message: `[Addon] DNS configured for ${addon.name}: ${result.clusterHost}`,
    });

    return {
      message: "DNS configured successfully.",
      clusterHost: result.clusterHost,
      srvConnectionString: result.srvConnectionString,
    };
  }

  /**
   * Remove DNS configuration for an addon.
   */
  async function removeDNS(id: string): Promise<{ message: string }> {
    const addon = await repo.getById(id);
    if (!addon) {
      throw new NotFoundError("Addon not found.");
    }

    const dnsConfig = addon.values?.dns;
    if (!dnsConfig?.enabled || !dnsConfig?.records?.length) {
      return { message: "DNS not configured for this addon." };
    }

    // Teardown records
    const dns = useDNSService();
    await dns.teardown(dnsConfig.records);

    // Update addon to remove DNS config
    await repo.updateById(id, {
      values: {
        ...addon.values,
        dns: { enabled: false },
      },
      connectionInfo: {
        ...addon.connectionInfo,
        srvConnectionString: undefined,
        dnsClusterHost: undefined,
      } as any,
    });

    logger.log({
      level: "info",
      message: "[Addon] DNS removed for " + addon.name,
    });

    return { message: "DNS configuration removed." };
  }

