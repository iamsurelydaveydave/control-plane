  /**
   * Configure S3 backup for a mongodb-replicaset addon.
   */
  async function configureBackup(
    id: string,
    config: {
      enabled: boolean;
      schedule?: string;
      retention?: number;
      s3: {
        bucket: string;
        endpoint?: string;
        region?: string;
        prefix?: string;
        accessKeyId: string;
        secretAccessKey: string;
      };
    }
  ): Promise<{ message: string }> {
    const addon = await repo.getById(id);
    if (!addon) {
      throw new NotFoundError("Addon not found.");
    }

    if (addon.type !== "mongodb-replicaset") {
      throw new BadRequestError("Backup configuration is only available for mongodb-replicaset type.");
    }

    // Update addon config
    const updatedConfig = {
      ...addon.config,
      backup: config,
    };

    // Update Helm values
    const updatedValues = {
      ...addon.values,
      backup: {
        enabled: config.enabled,
        schedule: config.schedule || "0 2 * * *",
        retention: config.retention || 7,
        s3: {
          bucket: config.s3.bucket,
          endpoint: config.s3.endpoint || "",
          region: config.s3.region || "us-east-1",
          prefix: config.s3.prefix || "mongodb-backups",
          accessKeyId: config.s3.accessKeyId,
          secretAccessKey: config.s3.secretAccessKey,
        },
      },
    };

    await repo.updateById(id, {
      config: updatedConfig,
      values: updatedValues,
    });

    // Upgrade Helm release with new backup config
    if (addon.status === "running") {
      const catalogEntry = getAddonCatalogEntry(addon.type);
      try {
        await helm.upgrade(
          addon.releaseName,
          catalogEntry.chart,
          addon.namespace,
          updatedValues,
          { wait: true, timeout: "5m" }
        );
        logger.log({
          level: "info",
          message: "[Addon] Backup configured for " + addon.name,
        });
      } catch (err: any) {
        logger.log({
          level: "error",
          message: "[Addon] Failed to apply backup config: " + err.message,
        });
        throw new InternalServerError("Failed to apply backup configuration: " + err.message);
      }
    }

    return {
      message: config.enabled
        ? "Backup configured. Schedule: " + (config.schedule || "0 2 * * *")
        : "Backup disabled.",
    };
  }

  /**
   * Trigger an on-demand backup for a mongodb-replicaset addon.
   */
  async function triggerBackup(id: string): Promise<{ message: string; jobName?: string }> {
    const addon = await repo.getById(id);
    if (!addon) {
      throw new NotFoundError("Addon not found.");
    }

    if (addon.type !== "mongodb-replicaset") {
      throw new BadRequestError("Backup is only available for mongodb-replicaset type.");
    }

    if (!addon.values?.backup?.enabled) {
      throw new BadRequestError(
        "Backup is not configured. Configure backup settings first."
      );
    }

    // Create a Job from the CronJob
    const k8s = useK8sService();
    const cronJobName = addon.releaseName + "-backup";
    const jobName = cronJobName + "-manual-" + Date.now();

    try {
      // Get the CronJob spec
      const cronJob = await k8s.getCronJob(addon.namespace, cronJobName);
      if (!cronJob) {
        throw new NotFoundError("Backup CronJob not found. Ensure backup is configured.");
      }

      // Create a Job from the CronJob template
      await k8s.createJobFromCronJob(addon.namespace, cronJobName, jobName);

      logger.log({
        level: "info",
        message: "[Addon] Manual backup triggered for " + addon.name + ": " + jobName,
      });

      return {
        message: "Backup job started.",
        jobName,
      };
    } catch (err: any) {
      logger.log({
        level: "error",
        message: "[Addon] Failed to trigger backup: " + err.message,
      });
      throw new InternalServerError("Failed to trigger backup: " + err.message);
    }
  }

